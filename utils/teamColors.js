/**
 * Get team colors for a given NBA team
 * Returns fallback blue/purple gradient if team not found
 */
export function getTeamColors(teamName) {
  return nbaTeamColors[teamName] || {
    primary: "oklch(0.50 0.15 245)",
    secondary: "oklch(0.50 0.18 285)",
    accent: "oklch(0.95 0.02 85)",
    gradient: "from-blue-600 to-purple-600"
  };
}

/**
 * Generate a gradient that blends two team colors
 * Used for game header backgrounds
 */
export function getGameGradient(homeTeam, awayTeam) {
  const homeColors = getTeamColors(homeTeam);
  const awayColors = getTeamColors(awayTeam);
  return `linear-gradient(135deg, ${homeColors.primary} 0%, ${awayColors.primary} 100%)`;
}

/**
 * Get team color with custom opacity
 * Uses CSS color-mix for OKLCH color space support
 */
export function getTeamColorWithOpacity(teamName, opacity = 0.1) {
  const colors = getTeamColors(teamName);
  return `color-mix(in oklch, ${colors.primary}, transparent ${100 - opacity * 100}%)`;
}
